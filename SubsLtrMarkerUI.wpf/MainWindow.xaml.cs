        private void OnBrowseFolderClicked(object sender, RoutedEventArgs e)
        {
            Ookii.Dialogs.Wpf.VistaFolderBrowserDialog dlg = new Ookii.Dialogs.Wpf.VistaFolderBrowserDialog();
            
            if(dlg.ShowDialog() == true)
            {
                ((ConverterViewModel)DataContext).FolderPath = dlg.SelectedPath;
            }    
        }
